<div class="box box-default box-solid collapsed-box">
            <div class="box-header with-border">
              <h3 class="box-title">¿Tu avance y asignacion de metas son iguales a InStoreView?</h3>

              <div class="box-tools pull-right">
                <button type="button" class="btn btn-box-tool" data-widget="collapse"><i class="fa fa-minus"></i>
                </button>
              </div>
              <!-- /.box-tools -->
            </div>
            <!-- /.box-header -->
            <div class="box-body" style="">
              <p>No, tus metas son creadas en este portal y tu avance proviene de ISV pero la distribucion es totalmente diferente.
              En el portal de ISV no hay una separacion de mercaderistas por lo cual si tu sucursal tuvo una venta de 100 cajas
              estas 100 cajas no son distribuidas en los mercaderistas, en la web de ISV, dado a que el 
              mercaderista es un dato anexo a la sucursal, si la sucursal tiene 2 mercaderistas a ambos les muestra 100 cajas de avance
              dado a que la distribucion de cajas en ISV solo llega hasta la sucursal, en cambio el portal de Prosud si hace esta distribucion,
              si la sucursal tiene 2 mercaderistas este mostrara 50 cajas para cada mercaderista.</p>
              <p>Ademas cabe la posibilidad de que tengamos diferencia sistematica, en lo cuales haya diferencia, por la creacion de cada uno de los productos que se venden en las distintas
                  sucursales, y que tengan  errores en sus respectivas unidades de medida (Ejemplo: La cantidad de unidades que tiene un producto por Caja) </p>
            <p>InStoreView es un punto de comparacion, sin embargo en la practica, ambas plataformas no tienen datos al 100% iguales. Dado a que ambas plataformas trabajan de manera diferente.</p>
              
            </div>
